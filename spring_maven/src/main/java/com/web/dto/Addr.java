package main.java.com.web.dto;

public class Addr {
	private String email;
	private String addr_type;
	private String main_addr;
	private String main_detail;

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getAddr_type() {
		return addr_type;
	}

	public void setAddr_type(String addr_type) {
		this.addr_type = addr_type;
	}

	public String getMain_addr() {
		return main_addr;
	}

	public void setMain_addr(String main_addr) {
		this.main_addr = main_addr;
	}

	public String getMain_detail() {
		return main_detail;
	}

	public void setMain_detail(String main_detail) {
		this.main_detail = main_detail;
	}

}
